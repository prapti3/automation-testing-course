package ex_04_Operator;

public class Lab004_Relational_Operators {
    public static void main(String[] args) {
        /*
        * 1.
    1. `<`  (Less than)
    2. `<=`   (Less than or equal to)
    3. `>`   (Greater than)
    4. `>=`   (Greater than or equal to)
    5. `==`   (Equal to)
    6. `!=`   (Not equal to)

        *
        * */


        //all of them will result in boolean output

        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_manisha = 33;
        int age_pramod = 34;
        boolean result = age_manisha <= age_pramod;
        System.out.println(result);









    }
}
