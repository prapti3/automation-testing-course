package ex_09_Switch;

public class Lab012_Interview {
    public static void main(String[] args) {
        char code = 'B';

        switch(code){
            default:
                System.out.println("Helloooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
