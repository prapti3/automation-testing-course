package ex_07_Increment_Decrement_OP;

public class Lab003_POST_ID {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(++a);
        System.out.println(a);

        //POST increment = Print first and the increase

        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
