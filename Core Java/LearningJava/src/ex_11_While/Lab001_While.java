package ex_11_While;

public class Lab001_While {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.print(i);
            i++;
        }
        System.out.println(" ");
//        I, C , U
        int j = 0;
        for(; j<10 ; j++){
            System.out.print(j);
        }
    }
}
