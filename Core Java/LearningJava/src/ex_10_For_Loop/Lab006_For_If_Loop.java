package ex_10_For_Loop;

public class Lab006_For_If_Loop {
    public static void main(String[] args) {
        for (int shruti = 0; shruti < 5; shruti++) {
            //0 to 17, 18 items
            if (shruti > 2) {
                System.out.println("Gift From Papa : iphone");
            } else {
                System.out.println("She will get only hugs! or Candy!");
            }
        }
    }
}
