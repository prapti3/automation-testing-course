package ex_10_For_Loop;

public class Lab003_VAR_NAME {
    public static void main(String[] args) {
        for(int aditi = 0; aditi < 5; aditi++) {
            System.out.println("Aditi: " + aditi);
        }

        for(int _1 = 0; _1 < 5; _1++) {
            System.out.println("_1 : "+ _1);
        }
    }
}
